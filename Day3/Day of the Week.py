var1=int(input("Enter number from 1-7 for week:"))
if var1==1:
    print("Monday")
elif var1==2:
    print("Tuesday")
elif var1==3:
    print("Wednesday")
elif var1==4:
    print("Thursday")
elif var1==5:
    print("Friday")
elif var1==6:
    print("Saturday")
elif var1==7:
    print("Sunday")
else:
    print("You have entered wrong number")
