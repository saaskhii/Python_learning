"""10. Dictionary Iteration: Iterate over the dictionary and print both keys and values.
"""
dict={
    "name":"Sakshi",
    "age":24,
    "email":"sakshig30820@gmail.com",
    "city":"Mumbai",
    "gender":"Female",
    "DOB":"30/08/2000"
}

for a,b in dict.items():
    print(f"{a}:{b}")
